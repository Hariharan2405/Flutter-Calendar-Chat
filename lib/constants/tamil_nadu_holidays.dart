  static String? getHolidayName(DateTime date) {
    final normalized = DateTime(date.year, date.month, date.day);
    final match = holidays.where(
      (h) => DateTime(h.date.year, h.date.month, h.date.day) == normalized,
    );
    if (match.isEmpty) return null;
    return match.map((h) => h.name).join(' & ');
  }

  static List<TamilNaduHoliday> getHolidaysForDate(DateTime date) {
    final normalized = DateTime(date.year, date.month, date.day);
    return holidays
        .where((h) => DateTime(h.date.year, h.date.month, h.date.day) == normalized)
        .toList();
  }

  static bool isHoliday(DateTime date) {
    final normalized = DateTime(date.year, date.month, date.day);
    return holidays.any(
      (h) => DateTime(h.date.year, h.date.month, h.date.day) == normalized,
    );
  }
